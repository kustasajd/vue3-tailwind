import { defineComponent } from "vue";
import Breadcrumb from "../../components/common/Breadcrumb.vue";
export default defineComponent({
  components: {
    Breadcrumb,
  },
  data: () => ({
    isLoading: true,
    linkItems: [],
    permissions: [],
    clients: [],
    charges: [],
    invoices: [],
    reports: [],
    payments: [],
    products: [],
    sites: [],
    users: [],
    licences: [],
    licenceTemplates: [],
    zones: [],
    admins: [],
    adminPer: [],
    floorplans: [],
    roomReservations: []
  }),
  created() {
    this.$store.dispatch("permissions/getAll");
  },
  watch: {
    "$store.state.permissions.all": function () {
      this.permissions = this.$store.state.permissions.all.items;
      if (this.permissions) {
        this.getPermissionByCategory(this.permissions);
      }
      this.checkLoading();
    },
  },
  methods: {
    checkLoading: function () {
      if (this.$store.state.permissions.all.items) {
        this.linkItems = this.getLinkItems();
        this.isLoading = false;
      }
    },
    getLinkItems: function () {
      return [
        {
          index: 1,
          title: "Akara Admin",
          name: "AkaraAdmin",
          params: null,
        },
        {
          index: 2,
          title: "Permissions",
          name: "Permissions",
          params: null,
        },
      ];
    },
    getPermissionByCategory: function (permissions) {
      permissions.forEach((permission) => {
        const category = permission.name.split(" ")[0];
        switch (category) {
          case "client":
            this.clients.push(permission);
            break;
          case "user":
            this.users.push(permission);
            break;
          case "product":
            this.products.push(permission);
            break;
          case "invoice":
            this.invoices.push(permission);
            break;
          case "report":
            this.reports.push(permission);
            break;
          case "zone":
            this.zones.push(permission);
            break;
          case "charge":
            this.charges.push(permission);
            break;
          case "licence":
            this.licences.push(permission);
            break;
          case "licence-template":
            this.licenceTemplates.push(permission);
            break;
          case "site":
            this.sites.push(permission);
            break;
          case "payment":
            this.payments.push(permission);
            break;
          case "akara":
            this.admins.push(permission);
            break;
          case "floorplan":
            this.floorplans.push(permission);
            break;
          case "room":
            this.roomReservations.push(permission);
            break;
          case "permission":
            this.adminPer.push(permission)
            break;
        }
      });
    },
  },
});
</script>